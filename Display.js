/* 

                All Style Display Possibilities
                
        [ innerHTML ]

        [ document.write ]
        
        [ window.alert ]
        
        [ console.log ]

        [ window.print ]

        [ alert ]

*/





/* [ =========================================================================================== ] */

/* 

    [ innerHTML ]

    innerHTML     ->  innerHTML  باستخدام  HTML الكتابة في عنصر 

    Examples : 

*/

document.getElementById("Test").innerHTML = 5 + 6;




/* [ =========================================================================================== ] */

/* 

    [ document.write ]

    document.write()     -> document.write()  باستخدام  HTML الكتابة في مخرجات  

    Examples : 

*/

document.write(5 + 6);







/* [ =========================================================================================== ] */

/* 

    [ window.alert ]

    window.alert()     -> window.alert () الكتابة في مربع تنبيه باستخدام 

    Examples : 

*/


window.alert(5 + 6);


/* [ =========================================================================================== ] */

/* 

    [ console.log ]

    console.log()     ->  console.log ()   الكتابة في وحدة تحكم المتصفح ، باستخدام    

    Examples : 

*/


console.log(5 + 6);


/* [ =========================================================================================== ] */

/* 

    [ window.print ]

    window.print()     ->   في المستعرض لطباعة محتوى النافذة الحالية window.print ()  استدعاء طريقة 

    Examples : 

*/


window.print();



/* [ =========================================================================================== ] */

/* 

    [ alert ]

    alert()     ->  مربع تنبيه

    Examples : 

*/

alert("Hello World !");